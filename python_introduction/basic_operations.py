number1 = 10
number2 = 5

def sum():
    return number1 + number2

def diff():
    return number1- number2

def prod():
    return number1 * number2

print(f"Addition of {number1} and {number2} is {sum()}")
print(f"Subtraction of {number1} and {number2} is {diff()}")
print(f"Multiplication of {number1} and {number2} is {prod()}")
